

int Solution::solve(int A) {
    long long a[2][2]={{1,0},{0,1}};
    long long b[2][2]={{1,1},{1,0}};
    
    while(A>0){
        if(A&1){
            long long x1 = (a[0][0]*b[0][0]+a[0][1]*b[1][0]+1000000007LL)%1000000007LL;
            long long y1 = (a[0][0]*b[0][1]+a[0][1]*b[1][1]+1000000007LL)%1000000007LL;
            long long z1 = (a[1][0]*b[0][0]+a[1][1]*b[1][0]+1000000007LL)%1000000007LL;
            long long w1 = (a[1][0]*b[0][1]+a[1][1]*b[1][1]+1000000007LL)%1000000007LL;
    
            a[0][0]=x1;
            a[0][1]=y1;
            a[1][0]=z1;
            a[1][1]=w1;
        }
        

        long long x = (b[0][0]*b[0][0]+b[0][1]*b[1][0]+1000000007LL)%1000000007LL;
        long long y = (b[0][0]*b[0][1]+b[0][1]*b[1][1]+1000000007LL)%1000000007LL;
        long long z = (b[1][0]*b[0][0]+b[1][1]*b[1][0]+1000000007LL)%1000000007LL;
        long long w = (b[1][0]*b[0][1]+b[1][1]*b[1][1]+1000000007LL)%1000000007LL;
    
        b[0][0]=x;
        b[0][1]=y;
        b[1][0]=z;
        b[1][1]=w;
        
        A>>=1;
    }
    
    return a[0][1];
}
